function h = circle(x,y,r, N)
hold on
th = 0:(2*pi)/(N-1):2*pi;
xunit = r * cos(th) + x;
yunit = r * sin(th) + y;
h = plot(xunit, yunit,'r');
hold off
end

function h = plot_sphere (x,y,z,r, N1,N2)
hold on
th = 0:(pi)/(N1-1):pi;
p = 0:(2*pi)/(N2-1):2*pi;
for j = 0:N1-1
	for k = 0:N2-1
		t_jk = (pi/(N1-1))*j;
		t_jkp1 = (pi/(N1-1))*(j+1);
		p_jk = (2*pi/(N2-1))*k;
		p_jkp1 = (2*pi/(N2-1))*(k+1);
		
		x_00 = r*sin(t_jk)*cos(p_jk) + x;
		y_00 = r*sin(t_jk)*sin(p_jk) + y;
		z_00 = r*cos(t_jk) + z;
		
		x_11 = r*sin(t_jkp1)*cos(p_jk) + x;
		y_11 = r*sin(t_jkp1)*sin(p_jk) + y;
		z_11 = r*cos(t_jkp1) + z;
		
		x_22 = r*sin(t_jk)*cos(p_jkp1) + x;
		y_22 = r*sin(t_jk)*sin(p_jkp1) + y;
		z_22 = r*cos(t_jk) + z;
		
		x_33 = r*sin(t_jkp1)*cos(p_jkp1) + x;
		y_33 = r*sin(t_jkp1)*sin(p_jkp1) + y;
		z_33 = r*cos(t_jkp1) + z;
		
		if (j < N1-1 && k < N2-1)
			fill3([x_00 x_11 x_22],[y_00 y_11 y_22],[z_00 z_11 z_22],'g','FaceAlpha',0.45);
			fill3([x_11 x_33 x_22],[y_11 y_33 y_22],[z_11 z_33 z_22],'g','FaceAlpha',0.45);
		end
	end
end

xunit = r .* sin(th).*cos(p) + x;
yunit = r .* sin(th).*sin(p) + y;
zunit = r .* cos(th) + z;
h = plot3(xunit, yunit, zunit, 'g');
hold off 
end

% Read and plot STL mesh using plot3
% Make sure your STL file is in the same folder or provide the full path

function [] = PlotSTL(s)

% Read STL file
[filename, pathname] = uigetfile('*.stl', 'Select an STL file');
if isequal(filename, 0)
    error('No STL file selected.');
end

% Read STL using built-in function (returns a triangulation object)
TR = stlread(fullfile(pathname, filename));

% Extract faces and vertices
faces = TR.ConnectivityList;
vertices = TR.Points;

% Loop through each triangle and plot edges with plot3
for i = 1:size(faces, 1)
    % Get indices of the triangle vertices
    idx = faces(i, :);
    
    % Get the vertex coordinates
    tri = vertices(idx, :);
    
    % Close the triangle by repeating the first vertex
    tri = [tri; tri(1, :)];
    
    % Plot the triangle edges
    figure(1)
    fill3(tri(:,1), tri(:,2), tri(:,3), 'g','FaceAlpha',0.75); % 'k' = black lines
end

end
